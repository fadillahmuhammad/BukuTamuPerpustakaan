    public void refreshTable(){
        //reset row pada tabel
        int jumlahRow = tblTamu.getRowCount();
        for(int i = jumlahRow - 1; i >= 0; i--){
            tblTamu.removeRow(i);
        }
        
        ResultSet data = modelTamu.getAllTamu();
        try {
            while(data.next()){
                tblTamu.addRow(new Object[]{
                    data.getString("Tanggal"),
                    data.getString("NIM"),
                    data.getString("Nama"),
                    data.getString("Jurusan"),
                    data.getString("Semester"),
                    data.getString("Ruang_Loket"),
                    data.getString("Keperluan"),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Buku_tamu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

