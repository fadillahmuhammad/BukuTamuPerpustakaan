    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        boolean insert = modelTamu.insertTamu(sdf.format(tf_tanggal.getDate()), tf_nim.getText(), tf_nama.getText(), tf_jurusan.getText(), Integer.parseInt(tf_semester.getText()), Integer.parseInt(tf_ruang_loket.getText()), tf_keperluan.getText());
        
        if(insert == true){
            refreshTable();
            tf_tanggal.setDate(null);
            tf_nim.setText("");
            tf_nama.setText("");
            tf_jurusan.setText("");
            tf_semester.setText("");
            tf_ruang_loket.setText("");
            tf_keperluan.setText("");
            
            JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void tbl_tamuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_tamuMouseClicked
        // TODO add your handling code here:
        int row = tbl_tamu.getSelectedRow();
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)tbl_tamu.getValueAt(row, 0));
            tf_tanggal.setDate(date);
            tf_nim.setText(tbl_tamu.getValueAt(row, 1).toString());
            tf_nama.setText(tbl_tamu.getValueAt(row, 2).toString());
            tf_jurusan.setText(tbl_tamu.getValueAt(row, 3).toString());
            tf_semester.setText(tbl_tamu.getValueAt(row, 4).toString());
            tf_ruang_loket.setText(tbl_tamu.getValueAt(row, 5).toString());
            tf_keperluan.setText(tbl_tamu.getValueAt(row, 6).toString());
            
            btn_update.setVisible(true);
            btn_hapus.setVisible(true);
            btn_simpan.setVisible(false);
        } catch (ParseException ex) {
            Logger.getLogger(Buku_tamu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbl_tamuMouseClicked

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        boolean update = modelTamu.updateTamu(sdf.format(tf_tanggal.getDate()), tf_nama.getText(), tf_jurusan.getText(), Integer.parseInt(tf_semester.getText()), Integer.parseInt(tf_ruang_loket.getText()), tf_keperluan.getText(), tf_nim.getText());
        
        if(update == true){
            refreshTable();
            tf_tanggal.setDate(null);
            tf_nama.setText("");
            tf_jurusan.setText("");
            tf_semester.setText("");
            tf_ruang_loket.setText("");
            tf_keperluan.setText("");
            tf_nim.setText("");
            
            JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate");
            
            btn_simpan.setVisible(true);
            btn_update.setVisible(false);
            btn_hapus.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Data Gagal Diupdate");
        }
    }//GEN-LAST:event_btn_updateActionPerformed

